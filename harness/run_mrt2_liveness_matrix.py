@dataclass(frozen=True)
class Arm:
    """One generation/decode arm derived from the frozen protocol."""

    arm_id: str
    prompt_id: str
    prompt: str
    seed: int
    mode: str
    refresh_seconds: float
    refresh_frames: int | None
    source_path: Path
    source_sha256: str
    output_dir: Path
    reuse_pilot: bool

    @property
    def manifest_path(self) -> Path:
        """Return the generator manifest path for this arm."""
        return self.output_dir / f"mlx-seed-{self.seed}-manifest.json"

    @property
    def tokens_path(self) -> Path:
        """Return the token-array path for this arm."""
        return self.output_dir / f"mlx-seed-{self.seed}-tokens.npy"

    @property
    def wav_path(self) -> Path:
        """Return the context-12 decoded WAV path for this arm."""
        return self.output_dir / "context12.wav"

    @property
    def decode_report_path(self) -> Path:
        """Return the decoder report path for this arm."""
        return self.wav_path.with_suffix(".json")

    @property
    def context0_wav_path(self) -> Path:
        """Return the neutral-context decoded WAV path for an unrefreshed arm."""
        return self.output_dir / "context0.wav"

    @property
    def context0_decode_report_path(self) -> Path:
        """Return the neutral-context decoder receipt path."""
        return self.context0_wav_path.with_suffix(".json")


@dataclass(frozen=True)
class Pair:
    """Matched refreshed and unrefreshed arms for one prompt and seed."""

    prompt_id: str
    seed: int
    refreshed: Arm
    unrefreshed: Arm
    analysis_dir: Path

    @property
    def analysis_path(self) -> Path:
        """Return the matched-pair analysis report path."""
        return self.analysis_dir / "pair-analysis.json"


@dataclass(frozen=True)
class Matrix:
    """Validated protocol values and their derived execution pairs."""

    protocol_path: Path
    protocol_sha256: str
    checkpoint_path: Path
    checkpoint_sha256: str
    seconds: int
    expected_frames: int
    temperature: float
    top_k: int
    decoder_context_frames: int
    arms: tuple[Arm, ...]
    pairs: tuple[Pair, ...]


def _sha256(path: Path) -> str:
    """Return the lowercase SHA-256 digest of ``path``."""
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def _load_json(path: Path) -> dict[str, Any]:
    """Load a JSON object and reject every other top-level type."""
    value = json.loads(path.read_text())
    if not isinstance(value, dict):
        raise ValueError(f"{path} must contain a JSON object")
    return value


def _repo_path(value: str | Path) -> Path:
    """Resolve a protocol or artifact path relative to the repository."""
    path = Path(value)
    return path if path.is_absolute() else REPO_ROOT / path


def _require_file_hash(path: Path, expected: object, label: str) -> None:
    """Require an existing file whose bytes match ``expected``."""
    if not path.is_file():
        raise ValueError(f"missing {label}: {path}")
    actual = _sha256(path)
    if not isinstance(expected, str) or actual != expected:
        raise ValueError(
            f"{label} hash mismatch: {path} has {actual}, expected {expected}"
        )


def _require_equal(actual: object, expected: object, label: str) -> None:
    """Raise a stable validation error when two protocol values differ."""
    if actual != expected:
        raise ValueError(f"{label}={actual!r}, expected {expected!r}")


def load_matrix(
    protocol_path: Path,
    *,
    matrix_root: Path,
    pilot_root: Path,
    expected_protocol_sha256: str,
) -> Matrix:
    """Load and fail-closed validate the frozen factorial protocol."""
    protocol_path = protocol_path.resolve()
    _require_file_hash(protocol_path, expected_protocol_sha256, "frozen protocol")
    protocol = _load_json(protocol_path)
    _require_equal(
        protocol.get("schema"), "mrt2-liveness-protocol-v1", "protocol schema"
    )
    _require_equal(
        protocol.get("status"),
        "frozen-before-candidate-generation",
        "protocol status",
    )

    generation = protocol.get("generation")
    if not isinstance(generation, dict):
        raise ValueError("protocol generation must be an object")
    seconds = generation.get("audibleSeconds")
    expected_frames = generation.get("expectedTokenFramesIncludingLookahead")
    temperature = generation.get("temperature")
    top_k = generation.get("topK")
    decoder_context = generation.get("decoderContextFrames")
    if not isinstance(seconds, int) or seconds <= 0:
        raise ValueError("protocol audibleSeconds must be a positive integer")
    if expected_frames != seconds * 25 + 1:
        raise ValueError("protocol expected token frames do not match 25 Hz")
    if not isinstance(temperature, (int, float)) or temperature <= 0:
        raise ValueError("protocol temperature must be positive")
    _require_equal(top_k, 40, "protocol topK")
    _require_equal(decoder_context, 12, "protocol decoderContextFrames")

    checkpoint = protocol.get("checkpoint")
    if not isinstance(checkpoint, dict):
        raise ValueError("protocol checkpoint must be an object")
    checkpoint_path = _repo_path(checkpoint.get("path", ""))
    checkpoint_sha256 = checkpoint.get("sha256")
    _require_file_hash(checkpoint_path, checkpoint_sha256, "checkpoint")

    fixtures_value = protocol.get("fixtures")
    if not isinstance(fixtures_value, list) or not fixtures_value:
        raise ValueError("protocol fixtures must be a nonempty list")
    if len(fixtures_value) != 4:
        raise ValueError("frozen protocol must contain exactly four fixtures")
    fixtures: dict[str, dict[str, Any]] = {}
    for fixture in fixtures_value:
        if not isinstance(fixture, dict) or not isinstance(fixture.get("id"), str):
            raise ValueError("every protocol fixture must have a string id")
        fixture_id = fixture["id"]
        if fixture_id in fixtures:
            raise ValueError(f"duplicate protocol fixture: {fixture_id}")
        source = fixture.get("sourceConditioning")
        if not isinstance(source, dict):
            raise ValueError(f"fixture {fixture_id} lacks source conditioning")
        source_path = _repo_path(source.get("path", ""))
        _require_file_hash(
            source_path, source.get("sha256"), f"{fixture_id} conditioning"
        )
        fixtures[fixture_id] = fixture

    modes_value = protocol.get("refreshModes")
    if not isinstance(modes_value, list):
        raise ValueError("protocol refreshModes must be a list")
    modes = {mode.get("id"): mode for mode in modes_value if isinstance(mode, dict)}
    _require_equal(set(modes), {"refresh10", "unrefreshed"}, "refresh modes")
    _require_equal(modes["refresh10"].get("refreshFrames"), 250, "refresh10 frames")
    _require_equal(
        modes["unrefreshed"].get("refreshFrames"), None, "unrefreshed frames"
    )

    seeds_value = protocol.get("seeds")
    if not isinstance(seeds_value, list) or not all(
        isinstance(seed, int) for seed in seeds_value
    ):
        raise ValueError("protocol seeds must be integers")
    seeds = tuple(seeds_value)
    if len(set(seeds)) != len(seeds):
        raise ValueError("protocol seeds must be unique")
    if len(seeds) != 3:
        raise ValueError("frozen protocol must contain exactly three seeds")

    arms_value = protocol.get("arms")
    if not isinstance(arms_value, list):
        raise ValueError("protocol arms must be a list")
    expected_keys = {
        (fixture_id, seed, mode_id)
        for fixture_id in fixtures
        for seed in seeds
        for mode_id in modes
    }
    raw_by_key: dict[tuple[str, int, str], dict[str, Any]] = {}
    for raw in arms_value:
        if not isinstance(raw, dict):
            raise ValueError("protocol arm must be an object")
        key = (raw.get("promptId"), raw.get("seed"), raw.get("mode"))
        if key in raw_by_key:
            raise ValueError(f"duplicate protocol arm: {key}")
        raw_by_key[key] = raw
    _require_equal(set(raw_by_key), expected_keys, "factorial arm keys")

    arms: list[Arm] = []
    arm_by_key: dict[tuple[str, int, str], Arm] = {}
    for fixture_id in fixtures:
        fixture = fixtures[fixture_id]
        source = fixture["sourceConditioning"]
        for seed in seeds:
            for mode_id in ("refresh10", "unrefreshed"):
                raw = raw_by_key[(fixture_id, seed, mode_id)]
                mode = modes[mode_id]
                _require_equal(
                    raw.get("sourceConditioningSha256"),
                    source.get("sha256"),
                    f"{raw.get('armId')} source hash",
                )
                _require_equal(
                    raw.get("refreshSeconds"),
                    mode.get("refreshSeconds"),
                    f"{raw.get('armId')} refresh seconds",
                )
                reuse_pilot = fixture_id == PILOT_PROMPT_ID and seed == PILOT_SEED
                output_dir = (
                    pilot_root / mode_id
                    if reuse_pilot
                    else matrix_root / fixture_id / f"seed-{seed}" / mode_id
                )
                arm = Arm(
                    arm_id=raw["armId"],
                    prompt_id=fixture_id,
                    prompt=fixture["prompt"],
                    seed=seed,
                    mode=mode_id,
                    refresh_seconds=float(mode["refreshSeconds"]),
                    refresh_frames=mode.get("refreshFrames"),
                    source_path=_repo_path(source["path"]),
                    source_sha256=source["sha256"],
                    output_dir=output_dir,
                    reuse_pilot=reuse_pilot,
                )
                arms.append(arm)
                arm_by_key[(fixture_id, seed, mode_id)] = arm

    pairs = tuple(
        Pair(
            prompt_id=fixture_id,
            seed=seed,
            refreshed=arm_by_key[(fixture_id, seed, "refresh10")],
            unrefreshed=arm_by_key[(fixture_id, seed, "unrefreshed")],
            analysis_dir=(
                pilot_root / "analysis-full-capture"
                if fixture_id == PILOT_PROMPT_ID and seed == PILOT_SEED
                else matrix_root / fixture_id / f"seed-{seed}" / "analysis"
            ),
        )
        for fixture_id in fixtures
        for seed in seeds
    )
    return Matrix(
        protocol_path=protocol_path,
        protocol_sha256=expected_protocol_sha256,
        checkpoint_path=checkpoint_path,
        checkpoint_sha256=checkpoint_sha256,
        seconds=seconds,
        expected_frames=expected_frames,
        temperature=float(temperature),
        top_k=top_k,
        decoder_context_frames=decoder_context,
        arms=tuple(arms),
        pairs=pairs,
    )


